RTR_CARDS_ORIG_CLIENT_PREFERENCE_out0 AS (

  SELECT * 
  
  FROM EXP_CARDS_ORIG_CLIENT_PREFERENCE AS in0
  
  WHERE ((CLID_CLIENT_PREFERENCE IS NULL) AND (ROW_NUMBER = 1))

),

RTR_CARDS_ORIG_CLIENT_PREFERENCE_EXPR_NEW_CLIENT_PREFERENCE AS (

  SELECT 
    CLID_CLIENT_ALIAS AS CLID_CLIENT_ALIAS1,
    CLID_CLIENT_PREFERENCE AS CLID_CLIENT_PREFERENCE1,
    MARKETING_OPT_IN_TEL AS MARKETING_OPT_IN_TEL1,
    MARKETING_OPT_IN_EMAIL AS MARKETING_OPT_IN_EMAIL1,
    MARKETING_OPT_IN_TEXT AS MARKETING_OPT_IN_TEXT1,
