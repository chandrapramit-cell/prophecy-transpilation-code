),

INS_CLIENT_PREFERENCE_EXP AS (

  SELECT 
    CLID_CLIENT_ALIAS1 AS CLID,
    SOURCE_SYSTEM_CODE1 AS SOURCE_SYSTEM_CODE,
    MARKETING_OPT_IN_POST1 AS MAIL_PREFERENCE,
    business_date1 AS MAIL_PREFERENCE_DATE,
    MARKETING_OPT_IN_TEL1 AS PHONE_PREFERENCE,
    business_date1 AS PHONE_PREFERENCE_DATE,
