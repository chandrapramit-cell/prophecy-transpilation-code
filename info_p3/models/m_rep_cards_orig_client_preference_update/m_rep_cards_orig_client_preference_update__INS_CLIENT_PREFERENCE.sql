    MAIL_PREFERENCE AS MAIL_PREFERENCE1,
    MAIL_PREFERENCE_DATE AS MAIL_PREFERENCE_DATE1,
    PHONE_PREFERENCE AS PHONE_PREFERENCE1,
    PHONE_PREFERENCE_DATE AS PHONE_PREFERENCE_DATE1,
    EMAIL_PREFERENCE AS EMAIL_PREFERENCE1,
    EMAIL_PREFERENCE_DATE AS EMAIL_PREFERENCE_DATE1,
    SOURCE_SYSTEM_CODE AS SOURCE_SYSTEM_CODE1,
    business_date AS business_date1,
    ROW_NUMBER AS ROW_NUMBER1,
    TOTAL_COMPARE AS TOTAL_COMPARE1
  
  FROM RTR_CARDS_ORIG_CLIENT_PREFERENCE_out0 AS in0

),

INS_CLIENT_PREFERENCE1_add_missing_column_0 AS (

  SELECT 
    CAST(NULL AS STRING) AS MPS_PREFERENCE,
    CAST(NULL AS TIMESTAMP) AS MPS_PREFERENCE_DATE,
    CAST(NULL AS STRING) AS TPS_PREFERENCE,
    CAST(NULL AS TIMESTAMP) AS TPS_PREFERENCE_DATE,
    CAST(NULL AS STRING) AS ICOS_PREFERENCE,
    CAST(NULL AS TIMESTAMP) AS ICOS_PREFERENCE_DATE,
