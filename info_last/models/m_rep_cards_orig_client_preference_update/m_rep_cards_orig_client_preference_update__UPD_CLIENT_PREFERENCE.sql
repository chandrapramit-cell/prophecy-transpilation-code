    CAST(NULL AS STRING) AS MPS_PREFERENCE,
    CAST(NULL AS STRING) AS MPS_PREFERENCE_DATE,
    CAST(NULL AS STRING) AS TPS_PREFERENCE,
    CAST(NULL AS STRING) AS TPS_PREFERENCE_DATE,
    CAST(NULL AS STRING) AS ICOS_PREFERENCE,
    CAST(NULL AS STRING) AS ICOS_PREFERENCE_DATE
  
  FROM UPD_CLIENT_PREFERENCE1_add_missing_column_0 AS in0

