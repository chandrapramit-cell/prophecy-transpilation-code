  FROM RTR_CARDS_ORIG_CLIENT_PREFERENCE_EXPR_UPDATE_CLIENT_PREFERENCE AS in0

),

UPD_CLIENT_PREFERENCE_EXP AS (

  SELECT 
    CLID_CLIENT_PREFERENCE3 AS CLID,
    SOURCE_SYSTEM_CODE3 AS SOURCE_SYSTEM_CODE,
    MARKETING_OPT_IN_POST3 AS MAIL_PREFERENCE,
    business_date3 AS MAIL_PREFERENCE_DATE,
    MARKETING_OPT_IN_TEL3 AS PHONE_PREFERENCE,
    business_date3 AS PHONE_PREFERENCE_DATE,
    MARKETING_OPT_IN_EMAIL3 AS EMAIL_PREFERENCE,
    business_date3 AS EMAIL_PREFERENCE_DATE,
    MARKETING_OPT_IN_TEXT3 AS SMS_PREFERENCE,
